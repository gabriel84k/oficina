     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        try {
            
            $user = Auth::user();

            $Cliente=User::with(['permisos', 'tipo','cliente'])
                                ->where('tipo_id',3)
                                ->where('empresa_id',$user->empresa_id)
                                ->orderby('name','ASC')
                                ->paginate(15);

            return \Response::json(['status'=>0,'descripcion'=>'Listado de Clientes','data'=>$Cliente, 'isTipo'=>$user->tipo_id]); 

        } catch (\Throwable $th) {
            #hacer el log.
            return \Response::json(['status'=>-1,'descripcion'=>'Error en Listado','data'=>$th->getMessage()]); 
        }
    }

